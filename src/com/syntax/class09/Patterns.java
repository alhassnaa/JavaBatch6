package com.syntax.class09;

public class Patterns {

	public static void main(String[] args) {
		
		for(int i=4;i<=5;i++) {
			
			for(int j=5;j>=1;j--) {
			
			System.out.print(i);
			}
			System.out.println();
		}
	
	System.out.println("---Printing Oattern----");
		
	
		for(int i=1;i<5;i++) {
		
			for(int j=1;j<=5;j++) {
		
				System.out.print(j);
				}
				System.out.println();
	}
	
	}
	

}
